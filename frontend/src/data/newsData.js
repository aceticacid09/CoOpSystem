// Helper function to check if news should be published
export const getPublishableNews = () => {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  
  return newsList.map(news => {
    const newsDate = new Date(news.date);
    newsDate.setHours(0, 0, 0, 0);
    
    // Auto-publish scheduled news if date has arrived
    if (news.status === 'scheduled' && newsDate <= today) {
      return { ...news, status: 'published' };
    }
    return news;
  });
};

// Filter only published news
export const getPublishedNews = () => {
  return getPublishableNews().filter(news => news.status === 'published');
};

